	public boolean isUnique(final EMaterial material) {
		if (EMaterial.cachedUnique.containsKey(material)) {
			return EMaterial.cachedUnique.get(material);
		}
		EMaterial[] values;
		for (int length = (values = values()).length, i = 0; i < length; ++i) {
			final EMaterial e = values[i];
			if (e.m.equalsIgnoreCase(material.m) && e.data > 0) {
				EMaterial.cachedUnique.put(material, true);
				return true;
			}
		}
		EMaterial.cachedUnique.put(material, false);
		return false;
	}

