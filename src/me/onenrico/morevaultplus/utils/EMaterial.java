	public static boolean isNewVersion() {
		if (EMaterial.newV == 0) {
			return false;
		}
		if (EMaterial.newV == 1) {
			return true;
		}
		final Material mat = Material.matchMaterial("RED_WOOL");
		if (mat != null) {
			EMaterial.newV = 1;
			return true;
		}
		EMaterial.newV = 0;
		return false;
	}

	public boolean isSameMaterial(final ItemStack comp) {
		if (isNewVersion()) {
			return comp.getType() == parseMaterial();
		}
		if (comp.getType() == parseMaterial() && comp.getData().getData() == data) {
			return true;
		}
		final EMaterial xmat = fromMaterial(comp.getType());
		return isDamageable(xmat) && parseMaterial() == comp.getType();
	}

	public static EMaterial fromMaterial(final Material mat) {
		try {
			return valueOf(mat.toString());
		} catch (IllegalArgumentException e) {
			return fromString(mat.toString());
		}
	}

	public static EMaterial matching(final String name, final byte data) {
		if (EMaterial.cachedSearch.containsKey(String.valueOf(name.toUpperCase()) + "," + data)) {
			return EMaterial.cachedSearch.get(String.valueOf(name.toUpperCase()) + "," + data);
		}
		EMaterial[] values;
		for (int length = (values = values()).length, i = 0; i < length; ++i) {
			final EMaterial mat = values[i];
			if (name.toUpperCase().equals(mat.m) && (byte) mat.data == data) {
				EMaterial.cachedSearch.put(String.valueOf(mat.m) + "," + data, mat);
				return mat;
			}
			if (name.toUpperCase().equals(mat.toString()) && (byte) mat.data == data) {
				EMaterial.cachedSearch.put(String.valueOf(mat.m) + "," + data, mat);
				return mat;
			}
		}
		return null;
	}

	public static EMaterial fromString(String key) {
		if (key != null) {
			key = key.toUpperCase();
		}
		EMaterial xmat = null;
		try {
			if (MathUT.strInt(key) > 0) {
				final int length = Material.values().length;
				int i = 0;
				while (i < length) {
					Material m = Material.values()[i];
					if (m.getId() == MathUT.strInt(key)) {
						if (MathUT.strInt(key) == 31) {
							m = Material.DEAD_BUSH;
						}
						final String[] split = key.split(":");
						if (split.length > 1) {
							return fromString(String.valueOf(m.toString()) + ":" + split[1]);
						}
						return fromString(m.toString());
					} else {
						++i;
					}
				}
			}
			xmat = valueOf(key);
			xmat.fromnew = true;
			return xmat;
		} catch (IllegalArgumentException e) {
			final String[] split2 = key.split(":");
			if (split2.length == 1) {
				xmat = matching(key, (byte) 0);
			} else {
				xmat = matching(split2[0], (byte) Integer.parseInt(split2[1]));
			}
			return xmat;
		}
	}

